package studentGrade.java;
public class Grade {
	private int mina;
	private int minb;
	private int minc;
	private int mind;
	public int getMina() {
		return mina;
	}
	public void setMina(int mina) {
		this.mina = mina;
	}
	public int getMinb() {
		return minb;
	}
	public void setMinb(int minb) {
		this.minb = minb;
	}
	public int getMinc() {
		return minc;
	}
	public void setMinc(int minc) {
		this.minc = minc;
	}
	public int getMind() {
		return mind;
	}
	public void setMind(int mind) {
		this.mind = mind;
	}
	

}
